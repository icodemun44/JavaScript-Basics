// sort for string is used to arrange the
// array in such way that the first index
// unmatched index of each element is compared
// and arranged alphabatically and also changed original array

let ar1 = ["c", "a", "b", "z"];
// for ascending sort
ar1.sort();

console.log(ar1);

// for descending sort

ar1.reverse();

console.log(ar1);
